package classes;

public class Produto4 {

    private String name;
    private double price;

    //COSTRUTOR

    public Produto4(String name, double price) {
        this.name = name;
        this.price = price;
    }

    //GETTERS E SETTERS

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

}
